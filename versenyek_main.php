	<h2>Versenyek</h2>
	<h3>SCA által szervezett versenyek</h3>
	<p>A hivatalos oldalakat és szabályokat az iconra kattintva éritek el!</p>
	<section>
		<a href="http://www.worldbrewerscup.org/"> <img src="pictures/WBRC-Icon.png" alt="WBRC"/></a>
		<h4>Brewers Cup Bajnokság</h4>
		<p>A versenyzőknek 10 perc áll rendelkezésükre, hogy elkészitsenek a három bíronak 1-1 filter kávét. Az elkészítés közben a lehető legtöbb hasznos információt át kell adni a zsűrinek, hogy miért ezt a kávét választották, a recepturát és mindent amit az eszkőzökről, kávéról fontosnak tartanak. A pontozásnál az ital íze a legfontosabb és annak tulajdonságai, és hogy ezt mennyire pontosan mondta el a versenyző, de nem elhanyagolhatók a technikai pontok se.</br></p>
	</section>
		
	<section>
		<a href="https://worldbaristachampionship.org/"> <img src="pictures/WBC-Icon.svg" alt="WBC"/></a>
		<h4>Barista Bajnokság</h4>
		<p>A király kategoriának is szokták nevezni, bár ezzel vitatkoznék. A baristának a szponzor által biztositott kávégépen kell bemutatnia a szerinte legjobb kávét presszó kávéként elkészitve a 4 birónak. Ezután kell csinálni nekik 1-1 cappuccinot, végezetül egy kreatív kávéital elkészitése a feladat amiben a kávénak kell hangsúlyosnak lenni. Mindezekre 15perc áll rendelkezésre. </br></p>

	</section>
	<section>
		<a href="https://www.worldcuptasters.org/"> <img src="pictures/WCTC-Icon.png" alt="WCTC"/></a>
		<h4>Cup Taster Bajnokság</h4>
		<p>A kávé kostóló verseny, feladat teljesitésére 8perc áll rendelkezésre. A versenyzők előtti aztalon van 8 szektor, minden szektorban 3 csésze, mindegyik csészében filter kávé technologiával készitett izesitéstől mentes tiszta kávé van. A három csészéből kettőben ugyan az az ital van az egyikben viszont más. Feladat kostolással megkeresni a kakukk tojást, minél rövidebb idő alatt. Hiszen az lesz a győztes aki a legtőbbet eltalálta, egyezésnél pedig az idő dönt.</br></p>
	</section>
	<section>
		<a href="https://www.worldlatteart.org/"> <img src="pictures/WLAC-Icon.png" alt="WLAC"/></a>
		<h4>Latte Art Bajnokság</h4>
		<p>Cappuccino és presszó macchiato-t kell a versenyzőknek késziteni és az italok tetejére mintát kell önteni, több foruló is van. Van amikor szabad kézzel kell önteni, és van amikor segédeszkőzzel lehet rajzolni az italba. 2 vizuális bíró értékeli az elkészült italokat, az íz nem számít.</br></p>
	</section>
	<section>
		<a href="https://www.worldcoffeeingoodspirits.org/"> <img src="pictures/WCIGS-Icon.svg" alt="WCIGS"/></a>
		<h4>Coffee in Good Spirit Bajnokság</h4>
		<p>A baristák és bártenderek versenyének ötvözése, itt alkoholos kávéitalokat kell késziteni. Hideg illetve meleg kreativ italokat a meghatározott kötelező alkoholt alapanyagul véve, de figyelve arra hogy a kávé legyen a domináns a többi alkoto elem csak kiegészití az italat. Ezenkivül mindenkinek el kell késziteni a szerintük legfinomabb írkávét. Itt bármilyen whiskey, cukor, kávé és tejszin használható.</br></p>
	</section>
